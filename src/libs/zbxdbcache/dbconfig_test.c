static int	saved_CONFIG_PINGER_FORKS,
		saved_CONFIG_POLLER_FORKS,
		saved_CONFIG_IPMIPOLLER_FORKS,
		saved_CONFIG_JAVAPOLLER_FORKS;

static int	cu_init_poller_forks()
{
	saved_CONFIG_PINGER_FORKS = CONFIG_PINGER_FORKS,
	saved_CONFIG_POLLER_FORKS = CONFIG_POLLER_FORKS,
	saved_CONFIG_IPMIPOLLER_FORKS = CONFIG_IPMIPOLLER_FORKS,
	saved_CONFIG_JAVAPOLLER_FORKS = CONFIG_JAVAPOLLER_FORKS;

	/* ATTENTION: only the case when all kinds of poller processes have been started is covered */

	while (0 == CONFIG_PINGER_FORKS)
		CONFIG_PINGER_FORKS = rand();

	while (0 == CONFIG_POLLER_FORKS)
		CONFIG_POLLER_FORKS = rand();

	while (0 == CONFIG_IPMIPOLLER_FORKS)
		CONFIG_IPMIPOLLER_FORKS = rand();

	while (0 == CONFIG_JAVAPOLLER_FORKS)
		CONFIG_JAVAPOLLER_FORKS = rand();

	return CUE_SUCCESS;
}

static int	cu_clean_poller_forks()
{
	CONFIG_PINGER_FORKS = saved_CONFIG_PINGER_FORKS;
	CONFIG_POLLER_FORKS = saved_CONFIG_POLLER_FORKS;
	CONFIG_IPMIPOLLER_FORKS = saved_CONFIG_IPMIPOLLER_FORKS;
	CONFIG_JAVAPOLLER_FORKS = saved_CONFIG_JAVAPOLLER_FORKS;

	return CUE_SUCCESS;
}

